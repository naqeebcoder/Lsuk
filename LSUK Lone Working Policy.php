    <h2>Policy Statement</h2>

      <p align="justify">
      <p>Language Service UK Limited ( hereafter referred to as: LSUK) is fully committed to comply and follow The Care Act 2014, which  is the primary piece of legislation in Great Britain, which allows the UK  introducing a statutory framework for safeguarding adults in England and the introduction of the Duty of Candour and Fundamental Standards by which all service providers are now inspected against.  LSUK believes that lone workers should not be at more risk than other employees. LSUK </p>
      <p>LSUK  understands lone workers to be those who work without close or direct supervision or company for substantial periods of time. LSUK interpreters who performs their duties at remote locations are hereby referred as lone workers. In this context LSUK Limited understands its duty as an employer being to assess any risks to lone workers and take steps to avoid or control those risks where necessary.</p>
      <p>LSUK  recognises that staff working alone in potentially isolate conditions have no immediate back up or support and so are at a greater risk of injury through aggression or violence directed towards them from service users, relatives, or the general public.<br>
       LSUK  also recognises that staff working alone need to rely on their own judgement and initiative and may be at a greater risk of making mistakes or errors. 
      </p>
      <p>LSUK  believes that training is particularly important for lone workers and research shows that adequate training is the single most critical factor in avoiding panic reactions in unusual situations. In particular lone workers need to be deemed competent to work alone, to be sufficiently experienced and to understand the risks and precautions needed fully.</p>
      <p>LSUK  understands its duty as an employer to ensure employees are competent to deal not only with the day to day facets of their work but with circumstances which are new, unusual or beyond the scope of their training, for example, if threatened with aggression and violence.</p>
      <p>LSUK  adheres fully to Outcome 14: Supporting Workers of the Essential Standards of Quality and Safety and Regulation 23: of the Health and Social Care Act 2008 (Regulated Activities) Regulations 2010, which relates to receiving appropriate training, professional development, supervision and appraisal.</p>

       <h3 style="margin-top:30px;">Lone Workers' Supervision Policy</h3>
       <p>By definition lone workers are those who work without constant supervision throughout their working day, therefore procedures must be put in place to equip lone workers to ensure they remain safe and to provide awareness training on a regular basis. </p>
        <p>LSUK Limited believes that supervision helps to ensure that employees understand the risks associated with their work and that the necessary safety precautions are carried out. The extent of supervision required depends on the risks involved and the ability of the lone worker to identify and handle health and safety issues.</p>

           <h3 style="margin-top:30px;">Lone Workers' Security Policy</h3>
           <p>When a member of staff visits a client in their own home he or she may be at risk through health and safety hazards in and around service users' homes and of physical or verbal assaults and hostility from service users, relatives and the general public. Recent evidence suggests that such incidents may be on the increase and home visiting protocols should take this into account, particularly in high risk areas such as high crime rate areas or visiting to interpret for people with mental health issues or anti-social behaviour. </p>
        
           <h5 style="margin-top:30px;">At LSUK Limited:</h5>
            <ul class="fa-ul">
             <li><i class="fa-li fa fa-check-square"></i>The assessment of all new referrals should include a risk assessment which includes threats from health and safety hazards and from aggression and violence and other threats to lone working</li>
             <li><i class="fa-li fa fa-check-square"></i>Lone workers should receive safeguarding training to ensure safety of themselves and others.</li>
             <li><i class="fa-li fa fa-check-square"></i>Lone workers should carry mobile phones so that they can summon help quickly; all phones should include an emergency number which will be attended at all times that staff are working</li>
             <li><i class="fa-li fa fa-check-square"></i>Administration staff in the central office should log and keep details of all home visits as well as having access to the names, addresses and telephone numbers of Service users</li>
             <li><i class="fa-li fa fa-check-square"></i>Administration staff in the central office should contact the duty manager in the event of any emergency situations</li>
             <li><i class="fa-li fa fa-check-square"></i>In a situation where a lone worker feels under immediate threat of their physical safety, they should contact the main office/on-call or the police directly. If contacting the main office/on-call the Manager/Directors would contact the police for them if required, the Manager/Directors should be careful to take all appropriate information from the lone worker, such as location and telephone number, and to pass this on to the police, after the incident the lone worker should fill in an incident for.</li>
            
           </ul>
          <P style="margin-top:20px;">It is strongly advised that staff carry in their cars the absolute minimum amount of equipment and that they always park their car in a well-lit, public place if at all possible. Thefts from cars are a major area of concern and muggings of care staff are a real threat, especially in high crime areas. If on foot then care staff should avoid dark, unlit, isolated routes to work.</P>
          <P>In cases where service is to be provided in a high crime area or to a service user with a known history of aggression or violence associated with them, then a full risk assessment should be completed by the supervisor/manager. Where there is significant risk then the support plan should be altered accordingly, either by reviewing the case with the relevant case manager or by requesting for alternative arrangements e.g. attendance in pairs.</P>
          <h3 style="margin-top:30px;">Untoward Incidents</h3>
            <p>Untoward incidents, including all incidents which involve the use or threat of aggression or violence, should be regularly reviewed and audited.</p>

            <h3 style="margin-top:30px;">Training</h3>
            <p>All staff should read this policy. As part of the induction process supervisors must satisfy themselves that each member of staff is competent and safe to work alone and that they are clear about how to act in ways that will maximise their own safety and about what to do in an emergency situation.</p>

           


