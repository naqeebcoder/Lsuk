       <h3 style="margin-top:30px;">1.	Introduction</h3>

       <p>Information is a vital asset for Language Service UK Limited (LSUK), both in terms of the management of bookings and of services and resources. It plays a key part in clinical governance, service planning and performance management. It is therefore of paramount importance that information is efficiently managed, and that appropriate policies, procedures, management accountability and structures provide a robust governance framework for information management. </p>

           <h3 style="margin-top:30px;">2.	The Purpose of the policy</h3>
           <p>This Information Governance Policy provides an overview of LSUK’s approach to information governance; a guide to the procedures in use; and details about the IG management structures within the organisation.  </p>
        
           <h3 style="margin-top:30px;">3.	The organisation’s approach to Information Governance</h3>
           <p>LSUK  undertakes to implement information governance effectively and will ensure the following: </p>

            <ul class="fa-ul">
             <li><i class="fa-li fa fa-check-square"></i>Information will be protected against unauthorised access;</li>
             <li><i class="fa-li fa fa-check-square"></i>Confidentiality of information will be assured;</li>
             <li><i class="fa-li fa fa-check-square"></i>Integrity of information will be maintained;</li>
             <li><i class="fa-li fa fa-check-square"></i>Information will be supported by the highest quality data;</li>
             <li><i class="fa-li fa fa-check-square"></i>Regulatory and legislative requirements will be met;</li>
             <li><i class="fa-li fa fa-check-square"></i>Business continuity plans will be produced, maintained and tested;</li>
             <li><i class="fa-li fa fa-check-square"></i>Information governance training will be available to all staff as necessary to their role;</li>
             <li><i class="fa-li fa fa-check-square"></i>All breaches of confidentiality and information security, actual or suspected, will be reported and investigated.</li>
            
           </ul>

      <h3 style="margin-top:30px;">4.	Procedures in use in the organisation</h3>
           <p>This Information Governance Policy is underpinned by the following procedures:</p>
             <ul class="fa-ul">
               <li><i class="fa-li fa fa-check-square"></i>Records management procedure that set outs how booking records will be created, used, stored and disposed of;</li>
               <li><i class="fa-li fa fa-check-square"></i>Access control procedure that sets out procedures for the management of access to computer-based information systems;</li>
               <li><i class="fa-li fa fa-check-square"></i>Information handling procedure that sets out procedures around the transfer of confidential information;</li>
               <li><i class="fa-li fa fa-check-square"></i>Incident management procedure that sets out the procedures for managing and reporting information incidents;</li>
               <li><i class="fa-li fa fa-check-square"></i>Business continuity plan that sets out the procedures in the event of a security failure or disaster affecting computer systems;</li>
              </ul> 
            <h3 style="margin-top:30px;">5.	Staff guidance in use in the organisation</h3>
            <p>Staff compliance with the procedures is supported by the following guidance material:</p>

            <ul class="fa-ul">
             <li><i class="fa-li fa fa-check-square"></i>Records management: guidelines on good record keeping;</li>
             <li><i class="fa-li fa fa-check-square"></i>Staff confidentiality code of conduct: sets out the required standards to maintain the confidentiality of information; obligations around the disclosure of information and appropriately obtaining patient consent;</li>
             <li><i class="fa-li fa fa-check-square"></i>Access control: guidelines on the appropriate use of computer systems;</li>
             <li><i class="fa-li fa fa-check-square"></i>Information handling: guidelines on the secure use of patient information;</li>
             <li><i class="fa-li fa fa-check-square"></i>Using mobile computing devices: guidelines on maintaining confidentiality and security when working with portable or removable computer equipment;</li>
             <li><i class="fa-li fa fa-check-square"></i>Information incidents: guidelines on identifying and reporting information incidents.</li>
            
           </ul>

            <h3 style="margin-top:30px;">6.	Responsibilities and accountabilities</h3>
            <p>The designated Information Governance lead for the organisation is Imran Shah.</p>
              <h4 style="margin-top:10px;">The key responsibilities of the lead are:</h4>
           
            <ul class="fa-ul">
             <li><i class="fa-li fa fa-check-square"></i>Developing and implementing IG procedures and processes for the organisation; </li>
             <li><i class="fa-li fa fa-check-square"></i>Raising awareness and providing advice and guidelines about IG to all staff; </li>
             <li><i class="fa-li fa fa-check-square"></i>Ensuring that any training made available is taken up; </li>
             <li><i class="fa-li fa fa-check-square"></i>Coordinating the activities of any other staff given data protection, confidentiality, information quality, records management and Freedom of Information responsibilities;</li>
            <li><i class="fa-li fa fa-check-square"></i>Ensuring that patient data is kept secure and that all data flows, internal and external are periodically checked against the Caldicott Principles;</li>
            <li><i class="fa-li fa fa-check-square"></i>Monitoring information handling in the organisation to ensure compliance with law, guidance and local procedures; </li>
            <li><i class="fa-li fa fa-check-square"></i>Ensuring patients are appropriately informed about the organisation’s information handling activities.</li>
           </ul>
    
           <p style="margin-top:20px;">The day to day responsibilities for providing guidance to staff will be undertaken by Manager.</p>
           <p>The partners/owner(s) of the organisation are responsible for ensuring that sufficient resources are provided to support the effective implementation of IG in order to ensure compliance with the law, professional codes of conduct and the NHS information governance assurance framework.</p>
           <p>All staff, whether permanent, temporary or contracted, and contractors are responsible for ensuring that they are aware of and comply with the requirements of this policy and the procedures and guidelines produced to support it.</p>


