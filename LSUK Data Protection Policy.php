    <h3>Background</h3>
      
      <p align="justify">
      <p>Language Services UK Limited (Hereafter referred to as: LSUK) is fully committed to comply and follow The Data Protectio Act 2018, which  is the primary piece of legislation that controls how personal information is used by organisations, businesses or the government in Great Britain. 
         The Data Protection Act 2018 states that everyone responsible for using personal data has to follow strict rules called ‘data protection principles’. They must make sure the information is:
      </p>

      <ul class="fa-ul">
        <li><i class="fa-li fa fa-check-square"></i>used fairly, lawfully and transparently</li>
        <li><i class="fa-li fa fa-check-square"></i>used for specified, explicit purposes</li>
        <li><i class="fa-li fa fa-check-square"></i>used in a way that is adequate, relevant and limited to only what is necessary</li>
        <li><i class="fa-li fa fa-check-square"></i>accurate and, where necessary, kept up to date</li>
        <li><i class="fa-li fa fa-check-square"></i>kept for no longer than is necessary</li>
        <li><i class="fa-li fa fa-check-square"></i>handled in a way that ensures appropriate security, including protection against unlawful or unauthorised processing, access, loss, destruction or damage</li>
      </ul>

              <p style="margin-top:20px;">
               There is stronger legal protection for more sensitive information, such as:
              </p>


      <ul class="fa-ul">
        <li><i class="fa-li fa fa-check-square"></i>Race</li>
