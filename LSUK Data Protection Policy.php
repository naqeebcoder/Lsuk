       <h3 style="margin-top:30px;">Data protection principles</h3>

       <p>LSUK is committed to processing data in accordance with its responsibilities under the GDPR.</p>
        <h5>LSUK is fully committed to Article "5" of the GDPR requires that personal data shall be:</h5>

        <ul class="fa-ul">
        <li><i class="fa-li fa fa-check-square"></i>“Processed lawfully, fairly and in a transparent manner in relation to individuals;</li>
        <li><i class="fa-li fa fa-check-square"></i>Collected for specified, explicit and legitimate purposes and not further processed in a manner that is incompatible with those purposes; further processing for archiving purposes in the public interest, scientific or historical research purposes or statistical purposes shall not be considered to be incompatible with the initial purposes;</li>
        <li><i class="fa-li fa fa-check-square"></i>Adequate, relevant and limited to what is necessary in relation to the purposes for which they are processed;</li>
        <li><i class="fa-li fa fa-check-square"></i>Accurate and, where necessary, kept up to date; every reasonable step must be taken to ensure that personal data that are inaccurate, having regard to the purposes for which they are processed, are erased or rectified without delay;</li>
        <li><i class="fa-li fa fa-check-square"></i>Kept in a form which permits identification of data subjects for no longer than is necessary for the purposes for which the personal data are processed; personal data may be stored for longer periods insofar as the personal data will be processed solely for archiving purposes in the public interest, scientific or historical research purposes or statistical purposes subject to implementation of the appropriate technical and organisational measures required by the GDPR in order to safeguard the rights and freedoms of individuals; </li>
        <li><i class="fa-li fa fa-check-square"></i>Processed in a manner that ensures appropriate security of the personal data,including protection against unauthorised or unlawful processing and against accidental loss, destruction or damage, using appropriate technical or organisational measures.” </li>
    
      </ul>    

