    <h3>IT SECURITY POLICY </h3>

      <p align="justify">
      <p>The Language Services UK Limited (Hereafter referred to as: LSUK) is responsibile to ensure that all legal obligations to maintain security and confidentiality including but not limited to the GDPR Regulations (2018) Data Protection Act (1998), Human Rights Act (1998), Copyright, Designs and Patents Act (1988), and Computer Misuse Act (1990) are met.</p>
      <p>The safe and effective use of IT including internet and e-mails are increasingly important in business today. As such, it is of utmost importance that LSUK computer systems are maintained and secure.</p>
      <p>LSUK seeks to protect its computer systems from misuse and to minimise the impact of service breaks by development of this Security Policy and procedures to manage and enforce it.Key purpose of the Security Policy is to ensure that:</p>

      <ul class="fa-ul">
        <li><i class="fa-li fa fa-check-square"></i>Computer systems are properly assessed, and an effective security regime established.</li>
        <li><i class="fa-li fa fa-check-square"></i>Procedures to detect and resolve security incidents are in place.</li>
        <li><i class="fa-li fa fa-check-square"></i>The confidentiality, integrity and availability of LSUK systems are maintained</li>
        <li><i class="fa-li fa fa-check-square"></i>Staff are aware of their responsibilities, roles and accountability when using LSUK  systems.</li>

      </ul>

       <h3 style="margin-top:30px;">IT manager roles and responsibilities</h3>

       <p>It is the responsibility of the LSUK IT manager to ensure that all legal obligations to maintain security and confidentiality including but not limited to the GDPR Regulations (2018) Data Protection Act (1998), Human Rights Act (1998), Copyright, Designs and Patents Act (1988), and Computer Misuse Act (1990) are met.</p>
         <h5>Other IT manager roles and responsibilities include;</h5>

         <ul class="fa-ul">
             <li><i class="fa-li fa fa-check-square"></i>Ensure all LSUK staff are instructed in their security responsibilities.</li>
             <li><i class="fa-li fa fa-check-square"></i>Ensure all staff using computer systems/media are fully trained.</li>
             <li><i class="fa-li fa fa-check-square"></i>Ensure no unauthorised staff are allowed to access any LSUK computer systems. </li>
             <li><i class="fa-li fa fa-check-square"></i>Determine which individuals are to be given access to each level of system data based upon on a job function and need and independent of status.</li>
             <li><i class="fa-li fa fa-check-square"></i>Ensure the relevant system managers are advised immediately about staff changes affecting computer access so that passwords etc. may be withdrawn or deleted.</li>
             <li><i class="fa-li fa fa-check-square"></i>Ensure that LSUK staff know how to report any security breaches, incidents, malfunctions and suspected system weaknesses or threats.</li>
             <li><i class="fa-li fa fa-check-square"></i>Monitor for actual or potential information security breaches.</li>
             <li><i class="fa-li fa fa-check-square"></i>Implement procedures to minimise exposure to fraud/theft/disruption of its systems.</li>
             <li><i class="fa-li fa fa-check-square"></i>Implement, monitor, document and communicate information security policy and guidance.</li>
             <li><i class="fa-li fa fa-check-square"></i>Report to the Senior Management Team as part of Management Review Meetings on the effectiveness of LSUK IT and Security policies and procedures, key risks and mitigation. </i>
             <li><i class="fa-li fa fa-check-square"></i>Advise on actual or potential breaches of confidentiality and recommend remedial action.</i>
             <li><i class="fa-li fa fa-check-square"></i>Ensure LSUK meets its legal responsibility under the Freedom of Information Act, advising staff on their legal responsibilities under the act and ensure a proper process exists for responding to requests of information in a timely manner.</i>
             <li><i class="fa-li fa fa-check-square"></i>Development and implementation of Information sharing protocols</i>
             <li><i class="fa-li fa fa-check-square"></i>Ensure LSUK equipment is sited or protected to reduce risks from environmental threats and hazards, and unauthorised access.</i>
             <li><i class="fa-li fa fa-check-square"></i>Ensure LSUK equipment purchases are security labelled, appropriate licensed software loaded, and suitably configured for use prior to addition to the LSUK assets list.</i>
             <li><i class="fa-li fa fa-check-square"></i>Allocate and configure individual user accounts and associated user authentication for each authorised user and any relevant network operating systems.</i>
             <li><i class="fa-li fa fa-check-square"></i>Authorise computer hardware disposal, delete it from the LSUK asset list and ensure data storage devices are purged of sensitive data prior to secure disposal.</i>

           </ul>



           <h3 style="margin-top:30px;">User responsibilities</h3>
           <p>Each member of LSUK Staff (this includes all permanently employed, contracted and voluntary staff members) and any other authorised users share a responsibility to maintain the security of LSUK information and systems and these are listed below;</p>
           <ul class="fa-ul">
             <li><i class="fa-li fa fa-check-square"></i>Each user is personally responsible for ensuring that no breaches of computer security result from their actions.</li>
             <li><i class="fa-li fa fa-check-square"></i>Must comply with LSUK’s relevant security and confidentiality policies and procedures.</li>
             <li><i class="fa-li fa fa-check-square"></i>Understand that breaches of policy will be investigated by formal disciplinary procedure which may lead to dismissal and/or legal action.</li>
             <li><i class="fa-li fa fa-check-square"></i>Is personally responsible for the accuracy and currency of the data they record on systems.</li>
             <li><i class="fa-li fa fa-check-square"></i>Must not disclose their passwords or allow anyone else to use their password or allow another user to work under their log on.</li>
             <li><i class="fa-li fa fa-check-square"></i>Should declare any potential conflicts of interest.</li>
             <li><i class="fa-li fa fa-check-square"></i>Must report any software or system malfunctions that it is believed could lead to a security incident to the IT Manager immediately.</li>
             <li><i class="fa-li fa fa-check-square"></i>Must report suspected security weaknesses or threats to the IT Manager.</li>
             <li><i class="fa-li fa fa-check-square"></i>All employees and relevant third-party users shall receive appropriate training and regular updates on LSUK’s policies and procedures.</i>
             <li><i class="fa-li fa fa-check-square"></i>Authorised users of LSUK systems shall receive appropriate system training before authorisation is granted.</i>
             <li><i class="fa-li fa fa-check-square"></i>Violations of this policy and associated procedures shall be dealt with through a formal disciplinary process and / or legal action.</i>
           </ul>

          
           <h3 style="margin-top:30px;">Registering users</h3>
           <p>Formal procedures will be used to control access to systems. The IT manager will approve each application for access and access privileges will be modified or removed, as appropriate, when an individual changes job or leaves. </p>
           <P>Access to systems will be based upon the staff member’s job role and relevant requirements. No individual will be given access to a live system unless properly trained and made aware of his or her security responsibilities.<br> Passwords are required which should be at least eight characters long and a mix of alphanumeric characters with sufficient complexity of structure to reflect the confidentiality of data held on LSUK systems. Users must keep their passwords secret, never disclose them to colleagues, and if requested to do so report that incident to their Line Manager.<br> Passwords should be changed at least every 180 days. </P>

           <h3 style="margin-top:30px;">Who do we provide these services to?</h3>
           <p>We are provisioning these services to :</p>

            <ul class="fa-ul">
             <li><i class="fa-li fa fa-check-square"></i>Advice and Support Service Providers</li>
             <li><i class="fa-li fa fa-check-square"></i>Charities and Trusts</li>
             <li><i class="fa-li fa fa-check-square"></i>Corporations and Large Businesses</li>
             <li><i class="fa-li fa fa-check-square"></i>Events Management Organisations</li>
             <li><i class="fa-li fa fa-check-square"></i>Law Firms</li>
             <li><i class="fa-li fa fa-check-square"></i>Mental Health Service Providers</li>
             <li><i class="fa-li fa fa-check-square"></i>Police, Fire and Ambulance Service Providers</li>
             <li><i class="fa-li fa fa-check-square"></i>Schools, Colleges and Universities</li>
             <li><i class="fa-li fa fa-check-square"></i>Social Services</li>
           </ul>

            <h3 style="margin-top:30px;">Equipment siting and protection</h3>
            <p>LSUK equipment will always be installed and sited in accordance with the manufacturer’s specification and with due consideration of Health and Safety legislation.<br> Equipment will be sited to reduce risks from environmental threats, and from unauthorised access. Where equipment must be kept in public areas, it will be positioned to reduce the risk of unauthorised access or casual viewing.<br> Environmental controls will be installed to protect central/key equipment. Such controls will trigger alarms if environmental problems occur. In such cases only authorised entry will be permitted.</p>
            <p>All central processing equipment, including file servers, will be covered by appropriate maintenance agreements or arrangements.</p>
            <p>Records of all faults/suspected faults will be maintained by the IT Manager supported by the administration support team. </p>
            <p>Any supplier requiring remote access to resolve system issues will be required to provide a written commitment to maintain confidentiality of data and information and only use qualified representatives.Each request for remote access will be authorised by the IT Manager.</p>
           

            <h3 style="margin-top:30px;">Security of equipment and data off premises</h3>
           
            <ul class="fa-ul">
             <li><i class="fa-li fa fa-check-square"></i>Hard disks on any machine and removable devices to backup/compress datamay contain sensitive/confidential data. Removal off site of such disks/storage media represents a potential threat.</li>
             <li><i class="fa-li fa fa-check-square"></i>Other than to transport it for a legitimate purpose, equipment and data will not be taken off site without approval of the appropriate line manager.</li>
             <li><i class="fa-li fa fa-check-square"></i>Portable computing devices must be provided with encryption and should not be left unattended.To preserve the integrity of data, frequent synchronisations should be made with system server computers. They should be maintained regularly, batteries kept charged to preserve their availability, and anti-virus software updated appropriately.</li>
           </ul>

           
           <h3 style="margin-top:30px;">Disposal of Equipment</h3>

           <p>Computer hardware disposal can only be authorised by the LSUK IT Manager who will ensure that data storage devices are irreversibly purged of sensitive data before disposal or they are securely destroyed. The procedures for disposal will be documented.</p>
           <p>Unusable computer media (e.g. floppy disks, magnetic tapes, CD-ROMS, USB keys) must also be securely destroyed.</p>

           <h3 style="margin-top:30px;">Licensed software</h3>

           <ul class="fa-ul">
            <li><i class="fa-li fa fa-check-square"></i>LSUK only permit approved software to be installed on its PCs.</li>
            <li><i class="fa-li fa fa-check-square"></i>Users must not download or load unauthorised software.</li>
            <li><i class="fa-li fa fa-check-square"></i>No newly acquired disks, magnetic media, or CDs, from whatever source are to be loaded unless they have been virus checked and approved by the IT Manager.</li>
            <li><i class="fa-li fa fa-check-square"></i>Users should report any viruses detected/suspected on their machines immediately to the IT Manager.</li>
            <li><i class="fa-li fa fa-check-square"></i>It is a criminal offence to make or use unauthorised copies of commercial software and offenders are liable to disciplinary action and civil or criminal prosecution.</li>
           
          </ul>

          <h3 style="margin-top:30px;">Data backup</h3>


           <ul class="fa-ul">
            <li><i class="fa-li fa fa-check-square"></i>All central systems have daily backup regimes. </li>
            <li><i class="fa-li fa fa-check-square"></i>Secure storage for the backups is geographically separate from the system location to protect against building loss.</li>
            <li><i class="fa-li fa fa-check-square"></i>The viability of central systems backups is tested when used in contingency tests.</li>
            <li><i class="fa-li fa fa-check-square"></i>All users must use the LSUK networks for any project documentation to ensure that back up of all key documents is maintained.</li>
           
          </ul>

          <h3 style="margin-top:30px;">Internet Usage</h3>
          <ul class="fa-ul">
            <li><i class="fa-li fa fa-check-square"></i> Internet use on Company time is authorized to conduct Company business only.Internet use brings the possibility of breaches to the security of confidential Company information and creates the possibility of contamination to our system via viruses or spyware which may allow unauthorized people outside the Company potential access to confidential information.</li>
            <li><i class="fa-li fa fa-check-square"></i>Removing such programs from the Company network requires investment of time and resources that is better devoted to progress. For this reason, and to assure the use of work time appropriately for work, we ask staff members to limit Internet use.</li>
            <li><i class="fa-li fa fa-check-square"></i>Additionally, under no circumstances may Company computers or other electronic equipment be used to obtain, view, or reach any pornographic, or otherwise immoral, unethical, or non-business-related Internet sites. Doing so can lead to disciplinary action up to and including termination of employment.</li>
            <li><i class="fa-li fa fa-check-square"></i>Limited, responsible use of LSUK Internet access during non-working hours such as a lunch break is acceptable however the use of social networking sites such as Facebook which may introduce unsafe access to the LSUK computer network will constitute a serious breach of internet policy and will not be tolerated at any time. </li>
           
          </ul>

          
          <h3 style="margin-top:30px;">Email Usage </h3>

          <ul class="fa-ul">
             <li><i class="fa-li fa fa-check-square"></i>Email Usage is to be used for Company business only.</li>
             <li><i class="fa-li fa fa-check-square"></i>Company data and information must not be shared outside of the Company without authorisation at any time. </li>
             <li><i class="fa-li fa fa-check-square"></i>Viewing pornography or sending pornographic jokes or stories via email is considered sexual harassment and will be treated as a disciplinary offence, as will any emails that discriminate against employees by virtue of any protected classification including race, gender, nationality, religion, and so forth.</li>
             <li><i class="fa-li fa fa-check-square"></i>You are also not to conduct personal business using the Company computer or email.</li>
             <li><i class="fa-li fa fa-check-square"></i>Please keep this in mind also as you consider forwarding non-business emails to associates, family or friends. Non-business-related emails waste company time and attention. Sending or forwarding non-business emails could result in disciplinary action that may lead to employment termination.</li>
             <li><i class="fa-li fa fa-check-square"></i>E-mail and Internet usage assigned to an employee's computer are solely for the purpose of conducting Company business. </li>
             <li><i class="fa-li fa fa-check-square"></i>Keep in mind that the Company owns any communication or information sent via email or that is stored on company equipment. Management and other authorized staff have the right to access any material in your email or on your computer at any time. Please do not consider your electronic communication, storage or access to be private if it is created or stored at work.</li>
           
           </ul>


          
          <h3 style="margin-top:30px;">Security Incidents</h3>
          <p>A security incident is an event that may result in:</p>

          <ul class="fa-ul">
             <li><i class="fa-li fa fa-check-square"></i>Degraded system integrity.</li>
             <li><i class="fa-li fa fa-check-square"></i>Loss of system availability.</li>
             <li><i class="fa-li fa fa-check-square"></i>Disclosure/loss or corruption of confidential information.</li>
             <li><i class="fa-li fa fa-check-square"></i>Disruption of activity.</li>
             <li><i class="fa-li fa fa-check-square"></i>Financial loss.</li>
             <li><i class="fa-li fa fa-check-square"></i>Legal action.</li>
             <li><i class="fa-li fa fa-check-square"></i>Unauthorised access to applications.</li>
             <li><i class="fa-li fa fa-check-square"></i>Misuse of software or access privileges</li>
             
           </ul>

           <P style="margin-top:20px;">All security incidents must be reported to the IT Manager immediately who will decide the most appropriate action to resolve or minimise impact on LSUK data or systems.<br> The incident will be further reported to the Senior Management Team who will take advice from the IT manager as to any further actions.<br> Any security incidents will be formally logged, categorised by severity and the resultant actions recorded.<br> Any major security incident will be immediately referred to the Senior Management Team for investigation.<br> The management and implementation of all IT and Security systems are subject toperiodic review by both internal and external auditors as part of LSUK’s Quality Management System.<br> This policy will be brought to the attention of all users and monitored in line with normal assurance processes.<br> This policy will be reviewed each year to ensure its continued suitability.</P>
             

