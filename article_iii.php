  <h2>3. In Conversation  with the Founder</h2>
  <p align="justify">
    Language Services UK is a young  and thriving organisation with a mission to help people from across the world  feel comfortable using all services available to them. With an intense drive to  make a difference, the group founder is proud to be expanding his outreach and making  a difference beyond his initial intentions.<br>
    Q: When did you notice a need for  Language Services<br>
    A: I&rsquo;d always had a keen interest  in translation and interpretation. I was initially self-taught and began as a  part-time freelancer, taking on roles as a hobby. During this period, I  realised just how diverse the UK was and whilst I was lucky enough to be fluent  in a number of languages, several people did not have this same luxury, in  fact, many faced difficulties with just the English language.<br>
    Q: What motivated you to set up  LSUK?<br>
    A: I studied during a time when  the world was facing the worst hit recession and difficult times taught me that  we could either fall or use our strengths to create positivity. I recognized  that what seemed like a frivolous past time was actually beneficial to the  people around me and helpful across the globe. In times when everyone seemed to  be struggling, the way to move out of this period was to work together and  connect across boundaries.<br>
    Q: What do you think are the most  important elements in translation and interpretation?<br>
    A: I think the things that are  most important in translation and interpretation are accuracy, confidentiality,  impartiality and consistency. It is imperative that we give both sides a true  depiction of what the other is saying, discretion is key as we work with  sensitive information. It is also vital that we remain neutral in all  discussions. We vet each of our translators to ensure that they have the tact and  compassion to deal with difficult matters in the most professional manner.<br>
    Q: What is LSUK trying to achieve  with its services?<br>
    A: For me personally, the service  we offer is first and foremost about the customer. We treat our clients with  the utmost kindness and compassion to ensure that we offer a service which is  excellent value for money with high satisfaction. Our aim is not to maximise  our profit but rather to use our skills to help people globally.<br>
    Q: What do you think is the  greatest potential for LSUK?<br>
    A: I started the company simply  to help people access important services within the UK. I firmly believe that  nobody should be deprived of opportunity because they do not understand, such  an attitude implies superiority and ignorance. Whilst our core value is a  desire to help individuals, as the world grows, we have become acutely aware of  the essential role we can play with business development. Not only are we  helping people coming into the UK, but also, we are unlocking doors for UK  businesses on an international scale. Language barriers are falling down and  our highly talented team are proud to be assisting in this process.</p>
